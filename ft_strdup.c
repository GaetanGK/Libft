#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*res;
	size_t	i;

	if (!(res = malloc(sizeof(char) * ft_strlen(s) + 1)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
